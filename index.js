const bodyElement = document.body;
const themeToggle = document.getElementById('themeToggle');
const languageToggle = document.getElementById('languageToggle');
const chatWidget = document.getElementById('chatWidget');
const chatToggleButton = document.getElementById('chatToggleButton');
const chatOpenButton = document.getElementById('chatToggle');
const authToggle = document.getElementById('authToggle');
const authModal = document.getElementById('authModal');
const authClose = document.getElementById('authClose');
const loginTab = document.getElementById('loginTab');
const signupTab = document.getElementById('signupTab');
const loginForm = document.getElementById('loginForm');
const signupForm = document.getElementById('signupForm');
const authName = document.getElementById('authName');
const authEmail = document.getElementById('authEmail');
const authEmailSignup = document.getElementById('authEmailSignup');
const authPassword = document.getElementById('authPassword');
const authPasswordSignup = document.getElementById('authPasswordSignup');
const chatClose = document.getElementById('chatClose');
const chatForm = document.getElementById('chatForm');
const chatInput = document.getElementById('chatInput');
const chatList = document.getElementById('chatList');
const footerYear = document.getElementById('footerYear');

let currentTheme = localStorage.getItem('theme') || 'light';
let currentLanguage = localStorage.getItem('language') || 'en';

function updateTheme(theme) {
  currentTheme = theme;
  bodyElement.classList.toggle('theme-dark', theme === 'dark');
  bodyElement.classList.toggle('theme-light', theme === 'light');
  themeToggle.textContent = theme === 'light' ? 'Dark' : 'Light';
  localStorage.setItem('theme', theme);
}

function updateLanguage(language) {
  currentLanguage = language;
  languageToggle.textContent = language === 'en' ? 'ES' : 'EN';
  localStorage.setItem('language', language);
  applyTranslations();
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
      element.textContent = translations[currentLanguage][key];
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
      element.placeholder = translations[currentLanguage][key];
    }
  });
}

function openChat() {
  chatWidget.classList.add('open');
  chatWidget.setAttribute('aria-hidden', 'false');
  chatInput.focus();
}

function closeChat() {
  chatWidget.classList.remove('open');
  chatWidget.setAttribute('aria-hidden', 'true');
}

function openAuthPanel() {
  authModal.classList.add('open');
  authModal.setAttribute('aria-hidden', 'false');
  switchAuthTab('login');
}

function closeAuthPanel() {
  authModal.classList.remove('open');
  authModal.setAttribute('aria-hidden', 'true');
}

function switchAuthTab(tabName) {
  const loginActive = tabName === 'login';
  loginTab.classList.toggle('auth-tab-active', loginActive);
  signupTab.classList.toggle('auth-tab-active', !loginActive);
  loginTab.setAttribute('aria-selected', loginActive.toString());
  signupTab.setAttribute('aria-selected', (!loginActive).toString());
  loginForm.classList.toggle('auth-form-active', loginActive);
  signupForm.classList.toggle('auth-form-active', !loginActive);
  const title = loginActive ? translations[currentLanguage].loginTitle : translations[currentLanguage].signupTab;
  document.getElementById('authTitle').textContent = title;
}

function showAuthSuccess(message) {
  alert(message);
  closeAuthPanel();
}

function handleAuthSubmit(event) {
  event.preventDefault();
  if (event.target === loginForm) {
    if (!authEmail.value || !authPassword.value) {
      alert(currentLanguage === 'es' ? 'Por favor completa todos los campos.' : 'Please complete all fields.');
      return;
    }
    showAuthSuccess(currentLanguage === 'es' ? 'Inicio de sesión exitoso.' : 'Login successful.');
  } else {
    if (!authName.value || !authEmailSignup.value || !authPasswordSignup.value) {
      alert(currentLanguage === 'es' ? 'Por favor completa todos los campos.' : 'Please complete all fields.');
      return;
    }
    showAuthSuccess(currentLanguage === 'es' ? 'Cuenta creada correctamente.' : 'Account created successfully.');
  }
}

function addChatMessage(message, sender = 'assistant') {
  const messageElement = document.createElement('li');
  messageElement.className = sender === 'user' ? 'user-message' : 'assistant-message';
  messageElement.textContent = message;
  chatList.appendChild(messageElement);
  chatBodyScroll();
}

function chatBodyScroll() {
  const body = document.getElementById('chatBody');
  if (body) {
    body.scrollTop = body.scrollHeight;
  }
}

function handleChatSubmit(event) {
  event.preventDefault();
  const message = chatInput.value.trim();
  if (!message) return;
  addChatMessage(message, 'user');
  chatInput.value = '';
  setTimeout(() => {
    addChatMessage(
      currentLanguage === 'es'
        ? 'Gracias por tu mensaje. Un especialista estará contigo en breve.'
        : 'Thanks for your message. A specialist will be with you shortly.'
    );
  }, 700);
}

themeToggle.addEventListener('click', () => {
  updateTheme(currentTheme === 'light' ? 'dark' : 'light');
});

languageToggle.addEventListener('click', () => {
  updateLanguage(currentLanguage === 'en' ? 'es' : 'en');
});

chatToggleButton.addEventListener('click', openChat);
chatOpenButton.addEventListener('click', (event) => {
  event.preventDefault();
  openChat();
});
authToggle.addEventListener('click', openAuthPanel);
authClose.addEventListener('click', closeAuthPanel);
loginTab.addEventListener('click', () => switchAuthTab('login'));
signupTab.addEventListener('click', () => switchAuthTab('signup'));
loginForm.addEventListener('submit', handleAuthSubmit);
signupForm.addEventListener('submit', handleAuthSubmit);
chatClose.addEventListener('click', closeChat);
chatForm.addEventListener('submit', handleChatSubmit);

authModal.addEventListener('click', (event) => {
  if (event.target === authModal) {
    closeAuthPanel();
  }
});

document.addEventListener('keydown', (event) => {
  if (event.key === 'Escape' && chatWidget.classList.contains('open')) {
    closeChat();
  }
});

document.addEventListener('click', (event) => {
  if (!chatWidget.contains(event.target) && !chatToggleButton.contains(event.target) && !chatOpenButton.contains(event.target)) {
    // keep chat visible until closed intentionally
  }
});

footerYear.textContent = new Date().getFullYear();
applyTranslations();
updateTheme(currentTheme);
updateLanguage(currentLanguage);
